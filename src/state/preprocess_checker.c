int	last_redir(char *str)
{
	while (!(*str == '<' && *(str - 1) == '<'))
	{
		if (*str == '\"' || *str == '\'')
			return (0);
		str--;
	}
	return (1);
}

int	is_controlchar(char c)
{
	if ((c >= ';' && c <= '>') || c == '|')
		return (1);
	return (0);
}
